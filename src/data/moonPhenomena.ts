// Get all phenomena as array
export const getAllPhenomena = (): MoonPhenomenon[] => {
  return Object.values(MOON_PHENOMENA);
};

// Get phenomenon by ID
export const getPhenomenonById = (id: string): MoonPhenomenon | undefined => {
  return MOON_PHENOMENA[id];
};

// Get phenomena by rarity
export const getPhenomenaByRarity = (rarity: MoonRarity): MoonPhenomenon[] => {
  return getAllPhenomena().filter((p) => p.rarity === rarity);
};

// Get count by rarity
export const getPhenomenaCountByRarity = (): Record<MoonRarity, number> => {
  const counts: Record<MoonRarity, number> = {
    normal: 0,
    rare: 0,
    very_rare: 0,
    legendary: 0,
    mythic: 0,
  };

  getAllPhenomena().forEach((p) => {
    counts[p.rarity]++;
  });

  return counts;
};
